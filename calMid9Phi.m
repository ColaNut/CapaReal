function [ Mid9Phi ] = calMid9Phi( Phi2 )
    
    Mid9Phi = zeros( 1, 9 );
    % Phi2 means: TwoLyrPhi

    Mid9Phi(1) = ( Phi2( 1, 1 ) + Phi2( 1, 2 ) + Phi2( 1, 4 ) + Phi2( 1, 5 ) ) / 8 ...
                + ( Phi2( 2, 1 ) + Phi2( 2, 2 ) + Phi2( 2, 4 ) + Phi2( 2, 5 ) ) / 8;
    Mid9Phi(2) = ( Phi2( 1, 2 ) + Phi2( 1, 5 ) ) / 4 ...
                + ( Phi2( 2, 2 ) + Phi2( 2, 5 ) ) / 4;
    Mid9Phi(3) = ( Phi2( 1, 2 ) + Phi2( 1, 3 ) + Phi2( 1, 5 ) + Phi2( 1, 6 ) ) / 8 ...
                + ( Phi2( 2, 2 ) + Phi2( 2, 3 ) + Phi2( 2, 5 ) + Phi2( 2, 6 ) ) / 8;
    Mid9Phi(4) = ( Phi2( 1, 4 ) + Phi2( 1, 5 ) ) / 4 ...
                + ( Phi2( 2, 4 ) + Phi2( 2, 5 ) ) / 4;
    Mid9Phi(5) = ( Phi2( 1, 5 ) + Phi2( 2, 5 ) ) / 2;
    Mid9Phi(6) = ( Phi2( 1, 5 ) + Phi2( 1, 6 ) ) / 4 ...
                + ( Phi2( 2, 5 ) + Phi2( 2, 6 ) ) / 4;
    Mid9Phi(7) = ( Phi2( 1, 4 ) + Phi2( 1, 5 ) + Phi2( 1, 7 ) + Phi2( 1, 8 ) ) / 8 ...
                + ( Phi2( 2, 4 ) + Phi2( 2, 5 ) + Phi2( 2, 7 ) + Phi2( 2, 8 ) ) / 8;
    Mid9Phi(8) = ( Phi2( 1, 5 ) + Phi2( 1, 8 ) ) / 4 ...
                + ( Phi2( 2, 5 ) + Phi2( 2, 8 ) ) / 4;
    Mid9Phi(9) = ( Phi2( 1, 5 ) + Phi2( 1, 6 ) + Phi2( 1, 8 ) + Phi2( 1, 9 ) ) / 8 ...
                + ( Phi2( 2, 5 ) + Phi2( 2, 6 ) + Phi2( 2, 8 ) + Phi2( 2, 9 ) ) / 8;
end